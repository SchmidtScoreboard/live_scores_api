#[derive(Debug, Clone, Eq, PartialEq)]
pub struct Team {
    pub id: u64,
    pub location: Cow<'static, str>,
    pub name: Cow<'static, str>,
    pub display_name: Cow<'static, str>,
    pub abbreviation: Cow<'static, str>,
    pub primary_color: Cow<'static, str>,
    pub secondary_color: Cow<'static, str>,
}

impl Team {
    const fn new_static(
        id: u64,
        location: &'static str,
        name: &'static str,
        display_name: &'static str,
        abbreviation: &'static str,
        primary_color: &'static str,
        secondary_color: &'static str
    ) -> Team {
        Team {
            id,
            location: Cow::Borrowed(location),
            name: Cow::Borrowed(name),
            display_name: Cow::Borrowed(display_name),
            abbreviation: Cow::Borrowed(abbreviation),
            primary_color: Cow::Borrowed(primary_color),
            secondary_color: Cow::Borrowed(secondary_color),
        }
    }

   pub fn new(
        id: u64,
        location: String,
        name: String,
        display_name: String,
        abbreviation: String,
        primary_color: String,
        secondary_color: String
    ) -> Team {
        Team {
            id,
            location: Cow::Owned(location),
            name: Cow::Owned(name),
            display_name: Cow::Owned(display_name),
            abbreviation: Cow::Owned(abbreviation),
            primary_color: Cow::Owned(primary_color),
            secondary_color: Cow::Owned(secondary_color),
        }
    }
}

